package com.cx.sjb.model;

public class Demo
{
    private Long id;

    private String name;

    public Demo()
    {
    }

    public Demo(String name)
    {
        this.name = name;
    }

    public Long getId()
    {
        return id;
    }

    public void setId(Long id)
    {
        this.id = id;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
}
